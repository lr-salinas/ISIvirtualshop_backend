package backend.project.dto;

public class MediaRequest {

    private String url;
    private Integer type;

    public MediaRequest(){
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }
}
